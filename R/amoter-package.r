#' amoter: analyse animal movement on realistic terrain
#'
#' The earth isn't flat! Yet, the movement of terrestrial animals is usually
#' only considered in the x,y-plane. For many types of questions and types of
#' terrain this is fine, but there are also many cases where elevation matters.
#' This package aims to help making such analyses.
#'
#' The package has functions to fetch elevation from a DEM at the track,
#' calculate attributes along the track related to the elevation change,
#' calculate important movement metrics for 3D tracks and compare
#' track orientation along terain features (like contours).
#' @name amoter
#' @docType package
